// 12条边的顶点索引对
const EDGE_VERTICES: [number, number][] = [
  [0, 1], [1, 2], [2, 3], [3, 0], // 底面
  [4, 5], [5, 6], [6, 7], [7, 4], // 顶面
  [0, 4], [1, 5], [2, 6], [3, 7], // 竖边
];

// edgeTable[256]：哪些边被等值面切割
const EDGE_TABLE: number[] = (() => {
  const table = new Array<number>(256);
  for (let i = 0; i < 256; i++) {
    let edgeFlags = 0;
    // 检查每条边的两个端点是否有不同符号
    for (let e = 0; e < 12; e++) {
      const [v0, v1] = EDGE_VERTICES[e];
      if (((i >> v0) & 1) !== ((i >> v1) & 1)) {
        edgeFlags |= (1 << e);
      }
    }
    table[i] = edgeFlags;
  }
  return table;
})();

